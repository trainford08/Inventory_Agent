/** All JTBDs that depend on a given feature. Used for M:N reverse lookup. */
export const JTBDS_BY_FEATURE: Record<string, string[]> = (() => {
  const map: Record<string, string[]> = {};
  for (const j of JTBDS) {
    for (const fid of j.features) {
      if (!map[fid]) map[fid] = [];
      map[fid].push(j.id);
    }
  }
  return map;
})();

