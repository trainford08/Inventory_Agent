export type StaysInAdo = "ado" | "gh" | "both" | "na";

export type Pattern = "P1" | "P2" | "P3" | "P4" | "P5" | "P6";

export type JtbdCategory =
  | "code"
  | "pull-requests"
  | "work-planning"
  | "ci-cd"
  | "deploy"
  | "testing"
  | "security"
  | "identity"
  | "automation"
  | "knowledge"
  | "reporting"
  | "code-comprehension"
  | "onboarding"
  | "review-workflow"
  | "admin"
  | "policy"
  | "incident"
  | "release"
  | "release-events";

export type Persona =
  | "Developer"
  | "Tech Lead"
  | "PM"
  | "Release Manager"
  | "QA"
  | "Security"
  | "Platform Engineer";

export type Frequency =
  | "Daily"
  | "Weekly"
  | "Per-sprint"
  | "Per-release"
  | "Per-project"
  | "Per-audit"
  | "Rare";

export type Jtbd = {
  id: string;
  name: string;
  category: JtbdCategory;
  categoryLabel: string;
  persona: Persona;
  frequency: Frequency;
  staysInAdo: StaysInAdo;
  staysReason: string;
  impact: string;
  /** Feature IDs this JTBD depends on. */
  features: string[];
};

export type Feature = {
  id: string;
  name: string;
  category: string;
  staysInAdo: StaysInAdo;
  /** Short reason from the canonical "Stays in ADO?" cell. */
  staysReason: string;
  pattern: Pattern | "na";
  preservation: "high" | "medium" | "low" | "gap" | "na";
  risk: "trivial" | "low" | "medium" | "high" | "na";
  /** Full canonical preservation strategy paragraph. */
  preservationStrategy: string;
  /** Raw "Depends on" text from canonical, e.g. "E15 Repository · E49 User". */
  dependsOn: string;
  /** Entity IDs this feature depends on (parsed from dependsOn). */
  entities: string[];
};

